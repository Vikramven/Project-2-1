package dice_chess.Board;

public class Coordinate{

    //X coordinate
    public int x;
    //Y coordinate
    public int y;

    /**
     * Constructor of the object, which contains the coordinate of the piece
     * @param x X coordinate
     * @param y Y coordinate
     */
    public Coordinate(int x, int y){
        this.x = x;
        this.y = y;
    }
}
